
public class Bacon {
	
	int port;
	String date;
	String time;
	String ipAdd;
	
	
	public Bacon(int port, String date, String time, String ipAdd) {
		super();
		this.port = port;
		this.date = date;
		this.time = time;
		this.ipAdd = ipAdd;
	}

	public int getPort() {
		return port;
	}
	
	

	public String getDate() {
		return date;
	}



	public String getTime() {
		return time;
	}



	public String getIpAdd() {
		return ipAdd;
	}

	

	
	
	
	

}
